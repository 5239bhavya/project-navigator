import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are an intelligent ERP Assistant for Shiv Furniture's business management system. You help admins and users work more efficiently.

Your capabilities:
- Answer questions about the system features (Purchase Orders, Sales Orders, Invoices, Bills, Budgets, Products, Contacts, Analytics)
- Provide guidance on best practices for inventory management, budgeting, and financial tracking
- Help users understand analytical accounts and budget allocation
- Offer tips for improving workflow efficiency
- Explain how different modules work together

Personality:
- Be friendly, professional, and concise
- Use bullet points for clarity when listing items
- Proactively suggest relevant features users might not know about
- If asked about data you don't have access to, politely explain you're an assistant and suggest where they can find that information in the app

Key system features to help with:
1. **Purchase Module**: Purchase Orders → Vendor Bills → Bill Payments
2. **Sales Module**: Sales Orders → Customer Invoices → Invoice Payments  
3. **Budgets**: Track income/expense against analytical accounts
4. **Analytical Accounts**: Cost centers for financial tracking
5. **Auto-Analytical Models**: Automatic account assignment rules
6. **Portal**: Customer/Vendor self-service portal

Always be helpful and guide users to the right part of the application.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please contact support." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service temporarily unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("AI Assistant error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
